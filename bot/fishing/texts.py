def pick(pool: Sequence[str]) -> str:
    return random.choice(tuple(pool))


def format_resources(energy: int, worms: int, maggots: int, rod_state: str) -> str:
    if rod_state == "ok":
        rod = "целая"
    elif rod_state == "broken":
        rod = "сломана — нужна новая"
    else:
        rod = "нет"
    return f"Энергия: {energy}/100 | Черви: {worms} | Опарыш: {maggots} | Удочка: {rod}"


def rod_label(rod_state: str) -> str:
    if rod_state == "ok":
        return "целая"
    if rod_state == "broken":
        return "сломана — нужна новая"
    return "нет"


def rod_hint(rod_state: str) -> str:
    if rod_state == "ok":
        return "целая"
    if rod_state == "broken":
        return "сломана — !рыбалка удочка"
    return "нет — !рыбалка удочка"
